package com.ags.hots.kelas;

public class ModelKelas {
    String id_kelas;
    String nama_kelas;
    String deskripsi;
    String foto;
    String harga;
    String status;

    public ModelKelas() {
    }

    public String getId_kelas() {
        return id_kelas;
    }

    public String getNama_kelas() {
        return nama_kelas;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public String getFoto() {
        return foto;
    }

    public String getStatus() {
        return status;
    }

    public String getHarga() {
        return harga;
    }

}
